import UIKit

extension UIFont{
    open class func roboto(size:CGFloat)->UIFont{
        return UIFont(name: "Roboto-Regular", size: size)!
    }
    open class func robotoBold(size:CGFloat)->UIFont{
        return UIFont(name: "Roboto-Bold", size: size)!
    }
    open class func robotoThin(size:CGFloat)->UIFont{
        return UIFont(name: "Roboto-Thin", size: size)!
    }
    open class func robotoItalic(size:CGFloat)->UIFont{
        return UIFont(name: "Roboto-Italic", size: size)!
    }
    open class func robotoBoldItalic(size:CGFloat)->UIFont{
        return UIFont(name: "Roboto-BoldItalic", size: size)!
    }
    open class func robotoBlack(size:CGFloat)->UIFont{
        return UIFont(name: "Roboto-Black", size: size)!
    }
    open class func robotoLightItalic(size:CGFloat)->UIFont{
        return UIFont(name: "Roboto-LightItalic", size: size)!
    }
    open class func robotoThinItalic(size:CGFloat)->UIFont{
        return UIFont(name: "Roboto-ThinItalic", size: size)!
    }
    open class func robotoMedium(size:CGFloat)->UIFont{
        return UIFont(name: "Roboto-Medium", size: size)!
    }
    open class func robotoMediumItalic(size:CGFloat)->UIFont{
        return UIFont(name: "Roboto-MediumItalic", size: size)!
    }
    open class func robotoLight(size:CGFloat)->UIFont{
        return UIFont(name: "Roboto-Light", size: size)!
    }
}
